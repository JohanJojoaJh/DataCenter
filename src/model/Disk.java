package model;

public class Disk
{
	//Attributes
	
	private double diskCapacity;
	
	//Methods
	
	public Disk(double diskCapacity)
	{
		this.diskCapacity = diskCapacity;
	}

	
	
	
	
	//Getters and setters
	
	public double getDiskCapacity()
	{
		return diskCapacity;
	}

	public void setDiskCapacity(double diskCapacity)
	{
		this.diskCapacity = diskCapacity;
	}
}